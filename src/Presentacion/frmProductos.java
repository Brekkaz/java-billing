    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        itemCombo ic=(itemCombo)cbCategoria.getSelectedItem();
        if(!(CamposVacios())){//create
            String[] valores= {txtCodigo.getText(),
                txtNombre.getText(),
                taDescripcion.getText(),
                ic.getId()};
            if(create){//insert
                if(usuario.TestRegistro(new String[]{"codigo"}, new String[]{txtCodigo.getText()})){
                    JOptionPane.showMessageDialog(null, "Ya Existe Un Producto Con Ese Codigo");
                }else{
                    JOptionPane.showMessageDialog(null, usuario.Create(valores));
                    LimpiarCampos();
                }
            }else{//update
                int op=JOptionPane.showConfirmDialog(null, "Realmente Desea Actualizar El Producto Codigo:"+this.Codigo+"?", "Confirmar", JOptionPane.OK_CANCEL_OPTION , JOptionPane.QUESTION_MESSAGE);
                if(op==0){
                    String[] campos= new String[]{"codigo", "nombre", "descripcion", "idcategoria"};
                    if(usuario.TestRegistroUpdate(campos, valores)){
                        JOptionPane.showMessageDialog(null, "Ya Existe Un Producto Con Esos Atributos");
                    }else{
                        valores[0]=null;
                        JOptionPane.showMessageDialog(null, usuario.Update(valores,new String[]{"codigo"},new String[]{txtCodigo.getText()}));
                        LimpiarCampos();
                    }
                }
            }
            LLenarTabla(tProductos, usuario.ReadAllInner(camposTabla));
        }else{
            JOptionPane.showMessageDialog(null, "Rellene Todos Los Campos");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

