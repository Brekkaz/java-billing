    private void btnNewCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewCatActionPerformed
        // TODO add your handling code here:
        String vNewCategoria =JOptionPane.showInputDialog(null, "Ingrese El Nombre De La Nueva Categoria", "Agregar Categoria", JOptionPane.INFORMATION_MESSAGE);
        if(vNewCategoria!=null && !"".equals(vNewCategoria)){

            Usuario user= new Usuario(new String[]{"categoria","La categoria"},null);
            if(user.TestRegistro(new String[]{"nombre"},new String[]{vNewCategoria})){
                JOptionPane.showMessageDialog(null, "Ya Existe Una Categoria Con Ese Nombre");
            }else{
                JOptionPane.showMessageDialog(null, user.Create(new String[]{null,vNewCategoria}));
                cbCategoria.setModel(usuario.LlenarCombo("categoria", 0, 1));
            }

        }
    }//GEN-LAST:event_btnNewCatActionPerformed

